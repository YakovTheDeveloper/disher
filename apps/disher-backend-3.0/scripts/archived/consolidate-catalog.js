/**
 * Regex-based prefix rules: applied if no explicit rename matched.
 * Order matters — first match wins.
 */
const PREFIX_RULES = [
  // "Специи, X" → "X" (capitalize first letter)
  { pattern: /^специи,\s+/i, replacement: '', capitalize: true },
];

/**
 * Suffixes to strip for normalization (applied AFTER prefix rules)
 */
const VARIETY_SUFFIXES = [
  /\s+(1|2)\s+кат\.?$/i,
  /,\s+сырой$/i,
  /,\s+с кожицей$/i,
  /,\s+без кожицы$/i,
];

/**
 * Получить базовое имя из названия продукта
 */
function getBaseProductName(name) {
  const lowerName = name.trim().toLowerCase();

  // 1. Exact rename lookup on original name
  if (EXPLICIT_RENAMES[lowerName]) {
    return EXPLICIT_RENAMES[lowerName];
  }

  let base = name.trim();

  // 2. Apply prefix rules (e.g. remove "Специи, " prefix)
  for (const rule of PREFIX_RULES) {
    if (rule.pattern.test(base)) {
      base = base.replace(rule.pattern, '').trim();
      if (rule.capitalize && base.length > 0) {
        base = base[0].toUpperCase() + base.slice(1);
      }
      break;
    }
  }

  // 3. Exact rename lookup AGAIN after prefix removal
  const lowerBase = base.toLowerCase();
  if (EXPLICIT_RENAMES[lowerBase]) {
    return EXPLICIT_RENAMES[lowerBase];
  }

  // 4. Strip variety suffixes
  for (const suffix of VARIETY_SUFFIXES) {
    base = base.replace(suffix, '').trim();
  }

  return base;
}

/**
 * Выбрать лучший ID для кластера
 * Приоритет: skurikhin > usda-foundation > usda-sr-legacy > custom
 */
function selectBestId(items) {
  const sourceOrder = {
    'skurikhin': 0,
    'usda-foundation': 1,
    'usda-sr-legacy': 2,
    'custom': 3,
  };

  return items.reduce((best, current) => {
    const bestScore = sourceOrder[best.source] ?? 999;
    const currentScore = sourceOrder[current.source] ?? 999;
    return currentScore < bestScore ? current : best;
  });
}

/**
 * Вычислить усреднённые нутриенты
 */
function averageNutrients(items) {
  const nutrientIds = new Set();
  items.forEach(item => {
    Object.keys(item.nutrients || {}).forEach(id => nutrientIds.add(id));
  });

  const result = {};
  nutrientIds.forEach(id => {
    const values = items
      .map(item => parseFloat(item.nutrients?.[id]))
      .filter(v => !isNaN(v));
    if (values.length > 0) {
      result[id] = Math.round((values.reduce((a, b) => a + b, 0) / values.length) * 100) / 100;
    }
  });

  return result;
}

/**
 * Выбрать каноничные порции (до 3 самых частых)
 */
function selectCanonicalPortions(items) {
  const portionCounts = {};
  const portionMap = {};

  items.forEach(item => {
    (item.portions || []).forEach(portion => {
      const key = `${portion.label}|${portion.grams}`;
      portionCounts[key] = (portionCounts[key] || 0) + 1;
      portionMap[key] = portion;
    });
  });

  return Object.entries(portionCounts)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 3)
    .map(([key]) => portionMap[key]);
}

/**
 * Основная функция консолидации
 */
async function consolidateCatalog() {
  console.log('Loading catalog...');
  const catalog = JSON.parse(fs.readFileSync(CATALOG_PATH, 'utf8'));
  console.log(`  Total products: ${catalog.length}`);

  // Группировать по базовому имени
  const clusters = {};
  catalog.forEach(item => {
    const base = getBaseProductName(item.name);
    if (!clusters[base]) clusters[base] = [];
    clusters[base].push(item);
  });

  console.log(`  Unique base products: ${Object.keys(clusters).length}`);

  // Создать объединённый каталог
  const consolidated = [];
  const mappings = [];

  Object.entries(clusters).forEach(([baseName, items]) => {
    const bestItem = selectBestId(items);

    if (items.length === 1) {
      // Rename to canonical name if different
      if (items[0].name !== baseName) {
        consolidated.push({ ...items[0], name: baseName });
        mappings.push({
          base_id: items[0].id,
          base_name: baseName,
          original_ids: [items[0].id],
          original_names: [items[0].name],
          merged_at: new Date().toISOString().split('T')[0],
          nutrients_source: `id_${items[0].id}`,
          action: 'renamed',
        });
      } else {
        consolidated.push(items[0]);
      }
    } else {
      // Объединить
      const mergedItem = {
        ...bestItem,
        name: baseName,
        portions: selectCanonicalPortions(items),
        nutrients: averageNutrients(items),
      };

      consolidated.push(mergedItem);

      mappings.push({
        base_id: bestItem.id,
        base_name: baseName,
        original_ids: items.map(i => i.id),
        original_names: items.map(i => i.name),
        merged_at: new Date().toISOString().split('T')[0],
        nutrients_source: items.length > 1 ? 'average' : `id_${bestItem.id}`,
        action: 'merged',
      });
    }
  });

  consolidated.sort((a, b) => a.name.localeCompare(b.name, 'ru'));

  const mergedCount = mappings.filter(m => m.action === 'merged').length;
  const renamedCount = mappings.filter(m => m.action === 'renamed').length;

  console.log(`\nDone!`);
  console.log(`  Merged clusters: ${mergedCount}`);
  console.log(`  Renamed items: ${renamedCount}`);
  console.log(`  Final product count: ${consolidated.length}`);
  console.log(`  Reduction: ${catalog.length} -> ${consolidated.length} (-${catalog.length - consolidated.length})`);

  // Сохранить консолидированный каталог
  fs.writeFileSync(OUTPUT_PATH, JSON.stringify(consolidated, null, 2), 'utf8');
  console.log(`\nSaved: ${OUTPUT_PATH}`);

  // Сохранить маппинг
  const mappingData = {
    version: '1.0',
    generated_at: new Date().toISOString().split('T')[0],
    total_merged_clusters: mergedCount,
    total_renamed: renamedCount,
    original_count: catalog.length,
    final_count: consolidated.length,
    merged_clusters: mappings.filter(m => m.action === 'merged'),
    renamed_items: mappings.filter(m => m.action === 'renamed'),
  };

  fs.mkdirSync(path.dirname(MAPPING_PATH), { recursive: true });
  fs.writeFileSync(MAPPING_PATH, JSON.stringify(mappingData, null, 2), 'utf8');
  console.log(`Mapping saved: ${MAPPING_PATH}`);

  // Топ объединений
  console.log('\nTop merged clusters:');
  mappings
    .filter(m => m.action === 'merged')
    .sort((a, b) => b.original_ids.length - a.original_ids.length)
    .slice(0, 15)
    .forEach((m, i) => {
      console.log(`  ${i + 1}. ${m.base_name} (${m.original_ids.length} variants <- ${m.original_names.join(', ')})`);
    });

  // Примеры переименований
  console.log('\nSample renames:');
  mappings
    .filter(m => m.action === 'renamed')
    .slice(0, 10)
    .forEach((m) => {
      console.log(`  "${m.original_names[0]}" -> "${m.base_name}"`);
    });
}

consolidateCatalog().catch(console.error);
