// Regenerate the stylelint ratchet baseline — the list of legacy SCSS files that
// currently violate the strict-value gate and are temporarily amnestied.
//
// Runs the real gate with the baseline disabled (STYLELINT_BASELINE_REGEN=1), then
// writes every still-failing file into stylelint-legacy-baseline.cjs. Run after a
// large refactor that moves/renames SCSS files (e.g. the FSD-flatten Phase B):
//   npm run lint:style:baseline
//
// The list only ever shrinks in normal work: touch a legacy file → tokenize it →
// it drops off on the next regen. Do not add files by hand.

import stylelint from 'stylelint';
import { writeFileSync } from 'node:fs';
import path from 'node:path';

process.env.STYLELINT_BASELINE_REGEN = '1';

const { results } = await stylelint.lint({
  files: 'src/**/*.scss',
  configFile: path.resolve('.stylelintrc.cjs'),
});

const failing = [
  ...new Set(
    results
      .filter((r) => r.warnings.length > 0 && r.source)
      .map((r) => path.relative(process.cwd(), r.source).split(path.sep).join('/')),
  ),
].sort();

const banner =
  '// AUTO-GENERATED by scripts/gen-stylelint-baseline.mjs — do not edit by hand.\n' +
  '// Legacy SCSS files amnestied from the strict-value gate (ratchet baseline).\n' +
  '// Regenerate: npm run lint:style:baseline\n\n';

writeFileSync(
  path.resolve('stylelint-legacy-baseline.cjs'),
  banner + 'module.exports = ' + JSON.stringify(failing, null, 2) + ';\n',
);

console.log(`stylelint baseline: ${failing.length} legacy files amnestied`);
